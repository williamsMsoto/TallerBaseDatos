        /* PARA VALIDAR EL FORMATO DEL CORREO*/
    public boolean validarFormatoCorreo(String correo) {
        Pattern pat = null;
        Matcher mat = null;
        pat = Pattern.compile("^([0-9a-zA-Z]([_.w][0-9a-zA-Z])@([0-9a-zA-Z][-w]*[0-9a-zA-Z].)+([a-zA-Z]{2,9}.)+[a-zA-Z]{2,3})$");
        mat = pat.matcher(correo);
        if (mat.find()) {
            return true;
        } else {
            return false;
        }
    }
    /*ACTIVAR AUTOMATICAMENTE EL BOTON DE GUARDAR*/
    public void validarIngreso() {
        String dni = txtDniTraba.getText().trim();
        int tamDni = dni.length();
        String nombre = txtNombreTraba.getText().trim();
        String apellido = txtApellidoTraba.getText().trim();
        boolean estado = validarFormatoCorreo(txtCorreoTraba.getText());
        String telf = txtTelefonoTraba.getText().trim();
        int telefono = telf.length();
        String contra = txtContraTraba.getText();
        Date fecha = calendario.getDate();
        if (tamDni != 8 || nombre.isEmpty() || apellido.isEmpty() || estado == true || telefono != 9  || contra.isEmpty() || fecha == null) {
            btnGuardar.setEnabled(false);
        } else {
            btnGuardar.setEnabled(true);
        }
    }

